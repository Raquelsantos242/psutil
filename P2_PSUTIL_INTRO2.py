import psutil

#informação sobre um processo específico por sua pid
p = psutil.Process(10200)
print(p)
print('------------------------------------------------------------------------------------------------------------------------')

#outras info
print(p.name())  #nome
print('------------------------------------------------------------------------------------------------------------------------')
print(p.exe())   #executável
print('------------------------------------------------------------------------------------------------------------------------')
print(p.cwd())   #diretório
print('------------------------------------------------------------------------------------------------------------------------')
print(p.status())#estado do processo
print('------------------------------------------------------------------------------------------------------------------------')
print(p.username())#usuário dono do processo
print('------------------------------------------------------------------------------------------------------------------------')
print(p.create_time())#momento da criação do processo(timestamp)
print('------------------------------------------------------------------------------------------------------------------------')


import time
print(time.ctime(p.create_time()))
print('------------------------------------------------------------------------------------------------------------------------')
print(p.cpu_times())#tempo de cpu
print('------------------------------------------------------------------------------------------------------------------------')
print(p.cpu_percent(interval=1.0))#% uso da cpu em 1s
print('------------------------------------------------------------------------------------------------------------------------')
print(p.cpu_affinity())#quais nucleos estao sendo utilizados no momento
print('------------------------------------------------------------------------------------------------------------------------')
print(p.memory_percent())#% de uso de memoria
print('------------------------------------------------------------------------------------------------------------------------')
print(p.memory_info())#info de uso de memoria do processo
print('------------------------------------------------------------------------------------------------------------------------')
print(p.num_threads())#numero de threads que o processo tem